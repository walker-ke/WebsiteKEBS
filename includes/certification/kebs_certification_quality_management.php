      This  second  revision of ISO 9001, which is identical to ISO 9001:2000, cancels  and  replaces  the  first revision - ISO 9001:1994, together with ISO  9002:1994  and  ISO  9003:1994.  It constitutes a technical revision of these documents and promotes process approach.   Those organizations which have used ISO 9002:1994 and ISO 9003:1994 in the past may use this standard by excluding certain requirements in accordance with clause 1.2 of ISO 9001:2000.</p>
