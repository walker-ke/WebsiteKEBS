    <td class="assessment_overview"><p align="justify" class="assessment_overview" style="font-size: 12px">International Organization for Standardization  (ISO) has published ISO 14000 series of standards with a view to provide  organizations world-over with a structure for managing environmental impacts of  their activities, products and services and to have a uniform international  Environmental Management Systems (EMS)for use as environmental management tool,  for achieving sustainable development, ISO 14000 is an evolving series of  generic standards which include a broad range of environmental  disciplines.&nbsp; </p>
        <p align="justify" class="assessment_overview" style="font-size: 12px">ISO 14000 addresses six distinct but related subjects like  Environmental Management Systems (EMS), Environmental Auditing (EA),  Environmental Performance Evaluation (EPE), Environmental Labelling (EL), Life  Cycle Assessment (LCA) and Environmental Aspects in Product Standards  (EAPS).&nbsp; Together, these standards provide the necessary guidance for  implementing an effective environmental management systems. </p>
