     <td valign="top" class="conference_tab_td"><p align="justify">The Kenya Bureau  of Standards (KEBS) understands the importance of impartiality in carrying out  its management system certification activities<br />
                      To this end,  KEBS has identified, analyzed and documented possibilities for conflict of  interests arising from provision of certification including any conflicts  arising from its relationships. KEBS manages these conflicts and ensures the  objectivity of its management system certification activities.</p>
                        <p align="justify"><strong>In particular:</strong><br />
                          When a  relationship poses an unacceptable threat to impartiality, then KEBS does not  provide certification</p>
                      <p align="justify">KEBS does not  provide certification services to other certification bodies for their management  systems.</p>
                      <p align="justify">KEBS does not  offer or provide management system consultancy.</p>
                      <p align="justify">KEBS does not  offer or provide internal audits to its certified clients and does not certify  a management system on which it provided internal audits.</p>
                      <p align="justify">KEBS does not  certify a management system on which a client has received management<br />
                        system  consultancy or internal audits, where the relationship between the consultancy organization  and KEBS poses an unacceptable threat to the impartiality of the certification body.</p>
                      <p align="justify">KEBS does not  outsource audits to management system consultancy organizations.</p>
                      <p align="justify">KEBS does not  offer or market its services as linked with the activities of an organization  that provides management system consultancy.<br />
                        To ensure that  there is no conflict of interests, personnel who have provided management<br />
                        system  consultancy, including those acting in a managerial capacity, are not used to  take part in an audit or other certification activities if they have been  involved in management system consultancy towards the client in question within  two years following the end of the consultancy.</p>
                      <p align="justify">KEBS takes  action to respond to any threats to its impartiality arising from the actions  of other persons, bodies or organizations.<br />
                        All  certification body personnel, either internal or external, or committees, who  could influence the certification activities, act impartially and do not allow  commercial, financial or other pressures to compromise impartiality.<br />
                        All personnel,  internal and external, are required to reveal any situation known to them that may  present them or the certification body with a conflict of interests.</p>
