     <td width="46">Step 1</td>
    <td width="138"> Preliminary Investigation</td>
  </tr>

<tr>
<td colspan="2">
<p>
When a client expresses interest in certification services, the KEBS CB determines whether the client is sufficiently prepared for the audit process. The client is requested to complete the Initial questionnaire, <a href="sqmt/certification/cb_downloads/download.php?file=CER_F_01 APPLICATION FOR REGISTRATION FOR MANAGEMENT SYSTEMS _REV. 01 - 19 Oct. 2016- FINAL.pdf"> APPLICATION FORM (Click here to Download)</a>. The client may also be issued with the sample contract upon request.
</p>
</td>
</tr>

<tr bgcolor="#0099FF" style="color:#FFF">
    <td>Step 2</td>
    <td>Application</td>
  </tr>
  <tr>
<td colspan="2">
<p>
If the client is found to be ready, a cost quotation is done, based on the information given in the <a href="sqmt/certification/cb_downloads/download.php?file=CER_F_01 APPLICATION FOR REGISTRATION FOR MANAGEMENT SYSTEMS _REV. 01 - 19 Oct. 2016- FINAL.pdf"> APPLICATION FORM</a> and send to the client and if this is acceptable to the client, the client pays the application fees and the HOD-CB sends the client a commitment letter to undertake the certification services.</p>
<p>
<b>Note:</b> Payment of application fees is an indication that the client has accepted the cost quotation and has read and understood the conditions of contract.
</p>
</td>
</tr>
  
<tr bgcolor="#0099FF" style="color:#FFF">
    <td>Step 3</td>
    <td> Stage One Audit</td>
  </tr>
  
  <tr>
<td colspan="2">
<p>
Certification audits are done in two stages; i.e. stage one and two.<br />
</p>
<p>
This stage one audit is done to:<br />
 Review the client's management system documented information against the normative document requirements.<br />
 Evaluate the client's site-specific conditions and to undertake discussions with the client's personnel to determine the preparedness for stage 2<br />
 Review the client's status and understanding regarding requirements of the standard, in particular with respect to the identification of key performance or significant aspects, processes, objectives and operation of the management system;<br />
 Review the allocation of resources for stage 2 and agree the details of stage 2 with the client<br />
 Evaluate if the client's internal audit and management reviews are being planned and performed and if they are effective<br />
 Provide focus for the stage 2 audit and agree with the client on the details for the stage two audit<br />
 To confirm information given in  the<a href="sqmt/certification/cb_downloads/download.php?file=CER_F_01 APPLICATION FOR REGISTRATION FOR MANAGEMENT SYSTEMS _REV. 01 - 19 Oct. 2016- FINAL.pdf"> APPLICATION FORM</a> and collect information regarding the scope of the audit<br />
For the initial certification audit, part of the stage one audit may be conducted at the client's premises and a documented conclusion on the fulfilment of stage 1 objectives and readiness for stage two including identification of any areas of concern that could<br />
be classified as nonconformities during stage 2 will be send to the client.<br />
</p>
<p>
The time period between the stage one and the stage two audit is dependent on the gaps identified during the stage one audit and is mutually agreed between the KEBS CB and the client. In any case this time period shall not be more than six months. If the six months lapse before the stage 2 audit is done, another stage 1 audit must be performed. If a client&acute;s management system is found, after the stage 1 audit, not to be ready for stage two, another stage 1 audit shall be performed at the appropriate time at the cost of the client.
</p>
</td>
</tr>
<tr bgcolor="#0099FF" style="color:#FFF">
    <td>Step 4</td>
    <td>Stage Two Audit</td>
  </tr>
  <tr>
<td colspan="2">
<p>
The stage two audit is done on site and its main objective is to evaluate the implementation well as the effectiveness of the client's management system.<br />
<p>
The audit includes an examination of at least the following:<br />
(a) information and evidence about conformity to all requirements of the applicable management system standard or other normative document<br />
(b) performance monitoring, measuring, reporting and reviewing against key performance objectives and targets (consistent with the expectations in the applicable management system standard or other normative document)<br />
(c) the client's management system and performance as regards legal compliance<br />
(d) operational control of the client's processes<br />
(e) internal auditing and management review processes<br />
(f) management responsibility for the client's policies<br />
(g) Links between the normative requirements, policy, performance objectives and targets (consistent with the expectations in the applicable management system<br />
Steps to Certification (KEBS CB) Rev 1 30 November 2015 Page 3/3<br />
standard or other normative document), any applicable legal requirements, responsibilities, competence of personnel, operations, procedures, performance data and internal audit findings and conclusions.
</p>
<p>
During this audit if non-conformities are found they shall be recorde are classified as either <u><strong>major</strong></u> or <u><strong>minor</strong></u>.<br />
</p>
<p>
Corrective action plans must be submitted for all nonconformities. The time for submitting of Corrective Action Plan (CAP) by the auditee/client is a maximum of 2 weeks and the auditor/CB must indicate acceptance of the plan before the client undertakes the actions therein.
</p>
<p>
For minor nonconformities the recommendation for certification can be made based on an acceptable Corrective Action Plan and the effectiveness of the corrective action shall then be evaluated at the next audit. The time for carrying out of the corrective action shall therefore be determined by the auditee/client and submitted as part of the corrective action plan.</p>
<p>
When major nonconformities are raised, the process of certification decision making shall not begin until the corrective actions have been undertaken by the client and effectiveness thereof been evaluated and found to be effective within a maximum of sixty days.
</p>
</td>
</tr>
 <tr bgcolor="#0099FF" style="color:#FFF">
    <td>Step 5</td>
    <td>Certification Decision</td>
  </tr>
  <tr>
<td colspan="2">
<p>
After the follow-up audit, during which time the corrective action plan from the stage two audit are confirmed to have been carried out, the audit findings and conclusions are evaluated by a peer audit and the certification officer who then makes recommendations to the certification committee. The committee makes the certification decision.
Once certified, the certificate is valid for 3 years.
</p>
</td>
</tr>
   <tr bgcolor="#0099FF" style="color:#FFF">
    <td>Step 6</td>
    <td>Surveillance</td>
  </tr>
  <tr>
<td colspan="2">
<p>
For the contract period, surveillance audits are carried at least once a calendar year except in recertification years.
</p>
</td>
</tr>
  <tr bgcolor="#0099FF" style="color:#FFF">
    <td>Step 7</td>
    <td>Recertification</td>
  </tr>
  <tr>
<td colspan="2">
<p>
Recertification audit shall be conducted at least two months before the expiry of the certification contract.
</p>
