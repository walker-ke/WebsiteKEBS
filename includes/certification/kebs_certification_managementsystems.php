                  <td valign="top" class="conference_tab_td"><h3><strong>Unlocking your potential</strong> <hr /></h3>
<p>The best businesses work as complete units with a shared vision. This may encompass information sharing, benchmarking, team working and working to the highest quality and environmental principles. <br /><br />A management system helps your organization to achieve these goals through a number of strategies, including process optimization, management focus and disciplined management thinking.</p>
<h3><strong>Why implement a management system?</strong> 
  <hr />
</h3>
<p>How do companies manage the multitude of challenges they are facing?  They develop processes and performance standards to measure and meet  these challenges; they integrate business principles in management  systems.</p>
<p>Some companies take a holistic approach, bringing  quality, environment, health and safety issues together in a single  system. However, many companies do not extract the full value from  their management systems because they are generally perceived as a tool  to maintain status quo rather than a means to manage change and  improvement. Yet an effective management system must add value to the  business, essentially by doing things better, cheaper or faster as the  system matures and develops.</p>
<p>The main management system standards  emphasise continual improvement. Implementing a management system gives  you the opportunity to focus on optimising the areas that matter most  to you and your stakeholders.</p>
<p>
    <br />
  Implementing an effective management system can help you to:</p>
<ul>
<li>Manage your social, environmental and financial risks</li>
<li>Improve operational effectiveness</li>
<li>Reduce costs</li>

<li>Increase customer and stakeholder satisfaction</li>
<li>Protect your brand and reputation</li>
<li>Achieve continual improvement</li>
<li>Promote innovation</li>
<li>Remove barriers to trade</li>
<li>Bring clarity to the marketplace</li></ul>
<p>By using a proven management system&nbsp;you're able to&nbsp;continually renew&nbsp;your mission, strategies, operations and service levels. <br /><br />
