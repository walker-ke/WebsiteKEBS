
<p><strong>Information Technology &ndash; Service  management System (ITSM)</strong></p>
<p><strong>ISO/IEC 20000</strong> ('ISO20k') is the international <strong>IT Service Management</strong> standard that enables IT organizations (whether in-house, outsourced or  external) to ensure that their IT service management processes are aligned both  with the needs of the business and with international best practice. . <br />
  Organizations can have their IT  service management systems independently certified as conforming to the  requirements of ISO/IEC 20000-1:2005. <br />
  Kenya  Bureau of Standards (KEBS) Certification Body (CB) is offering Certification to  ISO 20000:2005 standard.<br />
  <strong></strong><br />
  <strong>BENEFITS  OF ISO 20000:2005 STANDARD </strong> </p>
<ul>
  <li>Alignment  of information technology services and business strategy. </li>
  <li>Creation  of a formal framework for current service improvement projects </li>
  <li>Provides  a benchmark type comparison with best practices </li>
  <li>Creates  competitive advantage via the promotion of consistent and cost-effective  services. </li>
  <li>By  requiring ownership and responsibility at all levels, it creates a progressive  ethos and culture. </li>
  <li>Supports  'interchanging' of service providers and staff by virtue of the creation of  inter-enterprise operational processes. </li>
  <li>&nbsp;Reduction of risk and thus cost in terms of  external service receipt </li>
  <li>Through  the creation of a standard consistent approach, aids major organizational  changes. </li>
  <li>&nbsp;Enhanced reputation and perception </li>
  <li>&nbsp;Fundamental shift to pro-active rather than  re-active processes </li>
  <li>Improved  relationship between different departments via better definition and more  clarity in terms of responsibility and goals. </li>
  <li>Creation  of a stable framework for both resource training and service management  automation. </li>
</ul>
